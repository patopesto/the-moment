// TestParseGcodeFilamentUsage_MMFallback tests the [mm] → grams fallback when [g] is absent
func TestParseGcodeFilamentUsage_MMFallback(t *testing.T) {
	client := &PrusaLinkClient{}

	// 1000mm of 1.75mm PLA → roughly 2.97g
	gcode := `; filament used [mm] = 1000.00`

	usage, err := client.ParseGcodeFilamentUsage([]byte(gcode))
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if len(usage) != 1 {
		t.Fatalf("expected 1 entry, got %d", len(usage))
	}

	// Check within 10% of expected grams value
	expectedG := math.Pi * (1.75 / 2) * (1.75 / 2) * 1000.0 / 1000.0 * 1.24
	assertApprox(t, "toolhead 0 grams (mm fallback)", expectedG, usage[0].Grams, expectedG*0.10)
	assertApprox(t, "toolhead 0 mm", 1000.0, usage[0].MM, 0.01)
}

// TestParseGcodeFilamentUsage_BothGAndMM tests the typical PrusaSlicer case where both
// [g] and [mm] comments are present — mm must be captured alongside grams.
func TestParseGcodeFilamentUsage_BothGAndMM(t *testing.T) {
	client := &PrusaLinkClient{}

	gcode := `; generated by PrusaSlicer 2.8.0
; filament used [mm] = 4234.56, 1876.30
; filament used [cm3] = 10.10, 4.48
; filament used [g] = 12.52, 5.55
; filament used [cost] = 0.10, 0.04`

	usage, err := client.ParseGcodeFilamentUsage([]byte(gcode))
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if len(usage) != 2 {
		t.Fatalf("expected 2 entries, got %d: %v", len(usage), usage)
	}

	assertApprox(t, "T0 grams", 12.52, usage[0].Grams, 0.01)
	assertApprox(t, "T0 mm", 4234.56, usage[0].MM, 0.01)
	assertApprox(t, "T1 grams", 5.55, usage[1].Grams, 0.01)
	assertApprox(t, "T1 mm", 1876.30, usage[1].MM, 0.01)
}

