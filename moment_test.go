// TestParseGcodeFilamentUsage_SingleToolhead tests single-toolhead .gcode (PrusaSlicer comment)
func TestParseGcodeFilamentUsage_SingleToolhead(t *testing.T) {
	client := &PrusaLinkClient{}

	gcode := `; generated by PrusaSlicer 2.8.0
; filament used [g] = 12.45
G28 ; home all axes`

	usage, err := client.ParseGcodeFilamentUsage([]byte(gcode))
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if len(usage) != 1 {
		t.Fatalf("expected 1 toolhead, got %d", len(usage))
	}

	assertApprox(t, "toolhead 0 grams", 12.45, usage[0].Grams, 0.01)
	assertApprox(t, "toolhead 0 mm", 0, usage[0].MM, 0.001) // no [mm] in source
}

// TestParseGcodeFilamentUsage_MultiToolhead_INDX8 tests an 8-toolhead INDX scenario
func TestParseGcodeFilamentUsage_MultiToolhead_INDX8(t *testing.T) {
	client := &PrusaLinkClient{}

	// Simulate 8 toolheads — toolheads 2, 5 unused (0g)
	gcode := `; filament used [g] = 45.10, 30.20, 0.00, 12.50, 8.75, 0.00, 22.30, 5.60`

	usage, err := client.ParseGcodeFilamentUsage([]byte(gcode))
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	// Only non-zero entries should be in the map
	if len(usage) != 6 {
		t.Fatalf("expected 6 active toolheads, got %d: %v", len(usage), usage)
	}

	assertApprox(t, "toolhead 0", 45.10, usage[0].Grams, 0.01)
	assertApprox(t, "toolhead 1", 30.20, usage[1].Grams, 0.01)
	assertApprox(t, "toolhead 3", 12.50, usage[3].Grams, 0.01)
	assertApprox(t, "toolhead 4", 8.75, usage[4].Grams, 0.01)
	assertApprox(t, "toolhead 6", 22.30, usage[6].Grams, 0.01)
	assertApprox(t, "toolhead 7", 5.60, usage[7].Grams, 0.01)

	// Zero-weight toolheads must NOT be in the map
	if _, ok := usage[2]; ok {
		t.Error("toolhead 2 has 0g but appeared in usage map")
	}
	if _, ok := usage[5]; ok {
		t.Error("toolhead 5 has 0g but appeared in usage map")
	}
}

