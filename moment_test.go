// TestToolheadNameFormat verifies the location name format
func TestToolheadNameFormat(t *testing.T) {
	printerName := "Core One L"
	toolheadID := 3
	displayName := "T3-Black"

	locationName := fmt.Sprintf("%s - %s", printerName, displayName)
	expected := "Core One L - T3-Black"
	if locationName != expected {
		t.Errorf("expected '%s', got '%s'", expected, locationName)
	}

	// Default name format
	defaultName := fmt.Sprintf("%s - Toolhead %d", printerName, toolheadID)
	if defaultName != "Core One L - Toolhead 3" {
		t.Errorf("unexpected default name: %s", defaultName)
	}
}

