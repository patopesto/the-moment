// TestCancelledPrintScaling verifies partial filament calculation
func TestCancelledPrintScaling(t *testing.T) {
	fullUsage := map[int]float64{
		0: 100.0,
		1: 50.0,
	}

	progressPct := 60.0
	safetyMargin := 0.95
	scale := (progressPct / 100.0) * safetyMargin

	partialUsage := make(map[int]float64)
	for toolheadID, weight := range fullUsage {
		partialUsage[toolheadID] = weight * scale
	}

	// At 60% progress with 5% safety margin, expect 57% of full usage
	assertApprox(t, "toolhead 0 partial", 57.0, partialUsage[0], 0.1)
	assertApprox(t, "toolhead 1 partial", 28.5, partialUsage[1], 0.1)
}

// TestCancelledPrintAtZeroProgress verifies no deduction when progress is 0%
func TestCancelledPrintAtZeroProgress(t *testing.T) {
	progressPct := 0.0
	if progressPct <= 0 {
		// This is the guard condition in handlePrusaLinkPrintCancelled
		return // Correct — no deduction
	}
	t.Error("should have returned early for 0% progress")
}

