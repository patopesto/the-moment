// =============================================================================
// Helpers
// =============================================================================

func assertApprox(t *testing.T, label string, expected, actual, tolerance float64) {
	t.Helper()
	diff := math.Abs(expected - actual)
	if diff > tolerance {
		t.Errorf("%s: expected %.4f ± %.4f, got %.4f (diff %.4f)", label, expected, tolerance, actual, diff)
	}
}

func assertEqual(t *testing.T, label, expected, actual string) {
	t.Helper()
	if expected != actual {
		t.Errorf("%s: expected '%s', got '%s'", label, expected, actual)
	}
}
